package tech.project.schedule.model.enums;

/**
 * Enumeration of priority levels for tasks in the scheduling system.
 * Used to categorize and sort tasks based on their urgency and importance.
 */
public enum TaskPriority {
    CRITICAL, // Highest priority tasks requiring immediate attention
    IMPORTANT, // Tasks that are significant but not urgent
    OPTIONAL // Tasks that can be completed if time permits
}
